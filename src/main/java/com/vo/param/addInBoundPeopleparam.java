package com.vo.param;

public class addInBoundPeopleparam {
    /**
     * 地址
     */
    private String address;
    /**
     * 邮箱
     */
    private String email;
    /**
     * 入库人id
     */
    private String inBoundPersonId;
    /**
     * 姓名
     */
    private String name;
    /**
     * 联系电话
     */
    private String phone;
    /**
     * 备注
     */
    private String remark;
    /**
     * 入库人用户名
     */
    private String userName;

    public String getAddress() { return address; }
    public void setAddress(String value) { this.address = value; }

    public String getEmail() { return email; }
    public void setEmail(String value) { this.email = value; }

    public String getInBoundPersonId() { return inBoundPersonId; }
    public void setInBoundPersonId(String value) { this.inBoundPersonId = value; }

    public String getName() { return name; }
    public void setName(String value) { this.name = value; }

    public String getPhone() { return phone; }
    public void setPhone(String value) { this.phone = value; }

    public String getRemark() { return remark; }
    public void setRemark(String value) { this.remark = value; }

    public String getUserName() { return userName; }
    public void setUserName(String value) { this.userName = value; }
}
